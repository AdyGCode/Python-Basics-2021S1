# <     less than
# <=    less than or equals to
# >     greater than
# >=    greater than or equals to
# ==    equals to
# !=    not equals to
# and   Logical AND
# or    Logical OR
# not   Logical NOT

first = 15
second = 8
if first < second:
    print(f"{second} is bigger")

if first < 10 or second < 10:
    print(f"{first} or {second} is less than 10")


